    id INT AUTO_INCREMENT PRIMARY KEY,
    pseudo VARCHAR(50) NOT NULL,
    score INT NOT NULL,
    niveau VARCHAR(20) NOT NULL,
    date_partie DATETIME NOT NULL
);

CREATE TABLE mots (
    id INT AUTO_INCREMENT PRIMARY KEY,
    mot VARCHAR(50) NOT NULL,
    type_mot VARCHAR(50) NOT NULL,
    niveau VARCHAR(20) NOT NULL
);




